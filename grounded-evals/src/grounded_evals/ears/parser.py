"""Parse and render EARS requirements Markdown."""

from __future__ import annotations

import re
from uuid import uuid4

from grounded_evals.models.core import (
    EARSDocument,
    EARSPattern,
    EARSRequirement,
    GlossaryEntry,
    TraceabilityLink,
    TraceabilityLinkType,
)


class ParseError(ValueError):
    """Raised when Markdown cannot be parsed as an EARS document."""


class EARSParser:
    """Round-trip the project EARS document shape to and from Markdown."""

    def judge_spec_md(self, document: EARSDocument) -> str:
        """Render an evidence-backed LLM-as-Judge specification document."""
        lines: list[str] = [
            "# Requirements Document",
            "",
            f"**Agent:** {document.agent_name}",
            "",
            "## Introduction",
            "",
            document.introduction
            or (
                "Generated by GEDD from error analysis, domain annotations, "
                "and release-risk evidence."
            ),
            "",
        ]

        if document.glossary:
            lines.extend(["## Domain Glossary", ""])
            for entry in document.glossary:
                lines.append(
                    f"- **{entry.term}**: {entry.definition} "
                    f"(severity={entry.severity}, frequency={entry.frequency})"
                )
            lines.append("")

        lines.extend(["## Requirements", ""])
        for index, requirement in enumerate(document.sorted_requirements(), start=1):
            title = self._judge_requirement_title(requirement.name)
            lines.extend(
                [
                    f"### Requirement {index}: {title}",
                    "",
                    f"**User Story:** {requirement.user_story}",
                    "",
                    "#### Acceptance Criteria",
                    "",
                ]
            )

            criteria = requirement.acceptance_criteria or [requirement.ears_statement]
            for criterion_index, criterion in enumerate(criteria, start=1):
                rendered = self._render_judge_criterion(criterion)
                rendered_lines = rendered.splitlines() or [rendered]
                lines.append(f"{criterion_index}. {rendered_lines[0]}")
                for continuation in rendered_lines[1:]:
                    lines.append(f"   {continuation}")
            lines.extend(
                [
                    "",
                    "#### Evidence",
                    "",
                    f"- EARS Pattern: {requirement.pattern.value}",
                    f"- Priority: {requirement.priority_score:.2f}",
                    f"- Dimension: {requirement.dimension or 'quality'}",
                ]
            )
            if requirement.severity:
                lines.append(f"- Severity: {requirement.severity}")
            if requirement.frequency:
                lines.append(f"- Frequency: {requirement.frequency}")
            for link in requirement.traceability_links:
                lines.append(f"- Traceability: {link.link_type.value}: {link.target_label}")
            lines.append("")

        if document.non_functional_requirements:
            lines.extend(["## Spec Quality Gates", ""])
            for item in document.non_functional_requirements:
                lines.append(f"- {item}")
            lines.append("")

        return "\n".join(lines).rstrip() + "\n"

    def pretty_print(self, document: EARSDocument) -> str:
        lines: list[str] = [
            f"# {document.title}",
            "",
            f"Agent: {document.agent_name}",
            "",
        ]
        if document.introduction:
            lines.extend(["## Introduction", "", document.introduction, ""])

        if document.glossary:
            lines.extend(["## Glossary", ""])
            for entry in document.glossary:
                lines.append(
                    f"- **{entry.term}**: {entry.definition} "
                    f"(severity={entry.severity}, frequency={entry.frequency})"
                )
            lines.append("")

        lines.extend(["## Requirements", ""])
        for requirement in document.sorted_requirements():
            lines.extend(
                [
                    f"### {requirement.name}",
                    "",
                    f"- Pattern: {requirement.pattern.value}",
                    f"- Priority: {requirement.priority_score:.2f}",
                    f"- Dimension: {requirement.dimension or 'quality'}",
                    "",
                    f"User story: {requirement.user_story}",
                    "",
                    f"EARS: {requirement.ears_statement}",
                    "",
                    "Acceptance criteria:",
                ]
            )
            for criterion in requirement.acceptance_criteria:
                lines.append(f"- {criterion}")
            if requirement.traceability_links:
                lines.extend(["", "Traceability:"])
                for link in requirement.traceability_links:
                    lines.append(f"- {link.link_type.value}: {link.target_label}")
            lines.append("")

        if document.non_functional_requirements:
            lines.extend(["## Non-Functional Requirements", ""])
            for item in document.non_functional_requirements:
                lines.append(f"- {item}")
            lines.append("")

        return "\n".join(lines).rstrip() + "\n"

    def parse(self, markdown: str) -> EARSDocument:
        if not markdown.strip().startswith("# "):
            raise ParseError("EARS Markdown must start with an H1 title")

        title = markdown.splitlines()[0].removeprefix("# ").strip()
        if not title:
            raise ParseError("Missing document title")

        agent_name = self._extract_agent_name(markdown) or self._agent_from_title(title)
        introduction = self._extract_section(markdown, "Introduction")
        glossary = self._parse_glossary(markdown)
        requirements = self._parse_requirements(markdown)
        if not requirements:
            raise ParseError("No EARS requirements found")

        return EARSDocument(
            title=title,
            agent_name=agent_name,
            introduction=introduction,
            glossary=glossary,
            requirements=requirements,
            non_functional_requirements=self._parse_non_functional_requirements(markdown),
        )

    def _extract_agent_name(self, markdown: str) -> str:
        match = re.search(
            r"^(?:Agent:|\*\*Agent:\*\*)\s*(.+)$",
            markdown,
            flags=re.MULTILINE,
        )
        return match.group(1).strip() if match else ""

    def _agent_from_title(self, title: str) -> str:
        for marker in (" for ", " For "):
            if marker in title:
                return title.split(marker, 1)[1].strip()
        return "AI Agent"

    def _extract_section(self, markdown: str, heading: str) -> str:
        pattern = rf"^## {re.escape(heading)}\s*$([\s\S]*?)(?=^## |\Z)"
        match = re.search(pattern, markdown, flags=re.MULTILINE)
        return match.group(1).strip() if match else ""

    def _parse_glossary(self, markdown: str) -> list[GlossaryEntry]:
        section = self._extract_section(markdown, "Domain Glossary")
        if not section:
            section = self._extract_section(markdown, "Glossary")
        entries: list[GlossaryEntry] = []
        for line in section.splitlines():
            match = re.match(r"- \*\*(.+?)\*\*:\s*(.*)", line.strip())
            if match:
                entries.append(
                    GlossaryEntry(term=match.group(1).strip(), definition=match.group(2).strip())
                )
        return entries

    def _parse_requirements(self, markdown: str) -> list[EARSRequirement]:
        section = self._extract_section(markdown, "Requirements")
        if "#### Acceptance Criteria" in section or "**User Story:**" in section:
            return self._parse_judge_spec_requirements(section)

        return self._parse_project_requirements(section)

    def _parse_project_requirements(self, section: str) -> list[EARSRequirement]:
        chunks = re.split(r"^### ", section, flags=re.MULTILINE)
        requirements: list[EARSRequirement] = []
        for chunk in chunks:
            chunk = chunk.strip()
            if not chunk:
                continue
            lines = chunk.splitlines()
            name = lines[0].strip()
            body = "\n".join(lines[1:])
            pattern_value = self._extract_field(body, "Pattern") or EARSPattern.UBIQUITOUS.value
            priority = self._parse_float(self._extract_field(body, "Priority"), default=0.0)
            dimension = self._extract_field(body, "Dimension") or "quality"
            user_story = self._extract_inline(body, "User story") or ""
            ears_statement = self._extract_inline(body, "EARS")
            if not ears_statement:
                raise ParseError(f"Requirement '{name}' is missing an EARS statement")
            requirements.append(
                EARSRequirement(
                    name=name,
                    pattern=EARSPattern(pattern_value),
                    user_story=user_story,
                    acceptance_criteria=self._parse_acceptance_criteria(body),
                    ears_statement=ears_statement,
                    priority_score=priority,
                    dimension=dimension,
                    traceability_links=self._parse_traceability(body),
                )
            )
        return requirements

    def _parse_judge_spec_requirements(self, section: str) -> list[EARSRequirement]:
        headings = list(re.finditer(r"^###\s+(.+)$", section, flags=re.MULTILINE))
        requirements: list[EARSRequirement] = []
        for index, heading in enumerate(headings):
            body_start = heading.end()
            body_end = headings[index + 1].start() if index + 1 < len(headings) else len(section)
            body = section[body_start:body_end].strip()
            name = heading.group(1).strip()
            pattern_value = (
                self._extract_field(body, "EARS Pattern") or EARSPattern.UBIQUITOUS.value
            )
            priority = self._parse_float(self._extract_field(body, "Priority"), default=0.0)
            dimension = self._extract_field(body, "Dimension") or "quality"
            severity = int(self._parse_float(self._extract_field(body, "Severity"), default=0.0))
            frequency = int(self._parse_float(self._extract_field(body, "Frequency"), default=0.0))
            user_story = self._extract_bold_inline(body, "User Story")
            criteria = self._parse_numbered_acceptance_criteria(body)
            if not criteria:
                raise ParseError(f"Requirement '{name}' is missing acceptance criteria")
            try:
                pattern = EARSPattern(pattern_value)
            except ValueError:
                pattern = EARSPattern.UBIQUITOUS
            requirements.append(
                EARSRequirement(
                    name=name,
                    pattern=pattern,
                    user_story=user_story,
                    acceptance_criteria=criteria,
                    ears_statement=criteria[0],
                    priority_score=priority,
                    severity=severity,
                    frequency=frequency,
                    dimension=dimension,
                    traceability_links=self._parse_judge_spec_traceability(body),
                )
            )
        return requirements

    def _extract_field(self, body: str, field: str) -> str:
        match = re.search(rf"^- {re.escape(field)}:\s*(.+)$", body, flags=re.MULTILINE)
        return match.group(1).strip() if match else ""

    def _extract_inline(self, body: str, field: str) -> str:
        match = re.search(rf"^{re.escape(field)}:\s*(.+)$", body, flags=re.MULTILINE)
        return match.group(1).strip() if match else ""

    def _extract_bold_inline(self, body: str, field: str) -> str:
        match = re.search(rf"^\*\*{re.escape(field)}:\*\*\s*(.+)$", body, flags=re.MULTILINE)
        return match.group(1).strip() if match else ""

    def _parse_acceptance_criteria(self, body: str) -> list[str]:
        match = re.search(
            r"^Acceptance criteria:\s*$([\s\S]*?)(?=^Traceability:|\Z)",
            body,
            flags=re.MULTILINE,
        )
        if not match:
            return []
        return self._parse_bullets(match.group(1))

    def _parse_numbered_acceptance_criteria(self, body: str) -> list[str]:
        match = re.search(
            r"^#### Acceptance Criteria\s*$([\s\S]*?)(?=^#### |\Z)",
            body,
            flags=re.MULTILINE,
        )
        if not match:
            return []
        criteria: list[str] = []
        current: list[str] = []
        for raw_line in match.group(1).splitlines():
            line = raw_line.rstrip()
            if not line.strip():
                continue
            numbered = re.match(r"^\s*\d+\.\s+(.+)$", line)
            if numbered:
                if current:
                    criteria.append("\n".join(current).strip())
                current = [numbered.group(1).strip()]
            elif current:
                current.append(line.strip())
        if current:
            criteria.append("\n".join(current).strip())
        return criteria

    def _parse_bullets(self, text: str) -> list[str]:
        return [
            line.removeprefix("- ").strip()
            for line in text.splitlines()
            if line.strip().startswith("- ")
        ]

    def _parse_traceability(self, body: str) -> list[TraceabilityLink]:
        match = re.search(
            r"^Traceability:\s*$([\s\S]*?)(?=\Z)",
            body,
            flags=re.MULTILINE,
        )
        if not match:
            return []
        links: list[TraceabilityLink] = []
        for line in self._parse_bullets(match.group(1)):
            kind, _, label = line.partition(":")
            try:
                link_type = TraceabilityLinkType(kind.strip())
            except ValueError:
                link_type = TraceabilityLinkType.FAILURE_CODE
            links.append(
                TraceabilityLink(
                    link_type=link_type,
                    target_id=uuid4(),
                    target_label=label.strip(),
                    description="Parsed from EARS Markdown traceability section.",
                )
            )
        return links

    def _parse_judge_spec_traceability(self, body: str) -> list[TraceabilityLink]:
        links: list[TraceabilityLink] = []
        for line in body.splitlines():
            match = re.match(r"^- Traceability:\s*([^:]+):\s*(.+)$", line.strip())
            if not match:
                continue
            kind, label = match.groups()
            try:
                link_type = TraceabilityLinkType(kind.strip())
            except ValueError:
                link_type = TraceabilityLinkType.FAILURE_CODE
            links.append(
                TraceabilityLink(
                    link_type=link_type,
                    target_id=uuid4(),
                    target_label=label.strip(),
                    description="Parsed from judge spec evidence section.",
                )
            )
        return links

    def _parse_non_functional_requirements(self, markdown: str) -> list[str]:
        section = self._extract_section(markdown, "Non-Functional Requirements")
        if not section:
            section = self._extract_section(markdown, "Spec Quality Gates")
        return self._parse_bullets(section)

    def _parse_float(self, value: str, default: float) -> float:
        try:
            return float(value)
        except (TypeError, ValueError):
            return default

    def _judge_requirement_title(self, name: str) -> str:
        return re.sub(r"^(?:REQ|BASE)-\d+:\s*", "", name).strip() or name

    def _render_judge_criterion(self, criterion: str) -> str:
        text = criterion.strip()
        if "\n" in text:
            return text

        normalized = re.sub(r"\s+", " ", text).rstrip(".")
        event = re.match(r"(?i)^when (.+?), the system shall (.+)$", normalized)
        if event:
            return f"WHEN {event.group(1)}\nTHE SYSTEM SHALL {event.group(2)}."

        unwanted = re.match(r"(?i)^if (.+?), then the system shall (.+)$", normalized)
        if unwanted:
            return f"IF {unwanted.group(1)}\nTHEN THE SYSTEM SHALL {unwanted.group(2)}."

        state = re.match(r"(?i)^while (.+?), the system shall (.+)$", normalized)
        if state:
            return f"WHILE {state.group(1)}\nTHE SYSTEM SHALL {state.group(2)}."

        ubiquitous = re.match(r"(?i)^the system shall (.+)$", normalized)
        if ubiquitous:
            return f"THE SYSTEM SHALL {ubiquitous.group(1)}."

        return text
